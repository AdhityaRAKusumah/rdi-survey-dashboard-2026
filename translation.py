def normalize_display_case(value):
    if pd.isna(value):
        return pd.NA
    text = str(value).strip()
    if not text or text.lower() in {"nan", "none", "null"}:
        return pd.NA
    text = re.sub(r"\s+", " ", text.replace("_", " "))
    if text.isupper() or text.islower():
        cleaned = text.title()
    else:
        words = []
        for word in text.split(" "):
            words.append(word if any(char.isupper() for char in word[1:]) else word.capitalize())
        cleaned = " ".join(words)
    for source, target in ACRONYM_MAP.items():
        cleaned = re.sub(rf"\b{source}\b", target, cleaned)
    return cleaned


def translate_display_text(value):
    cleaned = normalize_display_case(value)
    if pd.isna(cleaned):
        return cleaned

    text = str(cleaned).strip()
    key = text.lower()
    if key in DISPLAY_TRANSLATIONS:
        return DISPLAY_TRANSLATIONS[key]

    translated = text
    for pattern, replacement in PHRASE_REPLACEMENTS:
        translated = re.sub(pattern, replacement, translated, flags=re.IGNORECASE)

    key = translated.strip().lower()
    if key in DISPLAY_TRANSLATIONS:
        return DISPLAY_TRANSLATIONS[key]

    return translated.strip()


def translate_label_text(value, max_length=None):
    raw_text = value
    if not pd.isna(value):
        raw_text = str(value)
        parts = raw_text.split("/")
        first_part = parts[0].strip().lower()
        has_section_prefix = (
            len(parts) > 1
            and (
                "assessment" in first_part
                or "profil" in first_part
                or "dampak" in first_part
                or re.match(r"^[a-z]\.", first_part)
            )
        )
        if has_section_prefix:
            raw_text = "/".join(parts[1:])
    text = translate_display_text(raw_text)
    if pd.isna(text):
        return text
    text = str(text)
    text = re.sub(r"^(?:[A-Za-z]\d+[a-z]?|\d+[A-Za-z]?|[a-z])[\s._-]*\d*\.?\s*", "", text).strip()
    for source, target in QUESTION_TRANSLATIONS:
        text = re.sub(re.escape(source), target, text, flags=re.IGNORECASE)
    for source, target in LABEL_RESIDUE_REPLACEMENTS:
        text = re.sub(re.escape(source), target, text, flags=re.IGNORECASE)
    text = re.sub(r"\s+", " ", text).strip()
    return text[:max_length] if max_length else text
